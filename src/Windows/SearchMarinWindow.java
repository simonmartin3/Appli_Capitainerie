    private void Button_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_SearchActionPerformed
        // TODO add your handling code here:
        
        boolean isFind = false;
        Vector <Marin> vMarin = new Vector<>();
        
        if(isNullOrEmpty(TextField_Search.getText()))
        {
            JOptionPane.showMessageDialog(new JFrame(), "Pas de marin entré !", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            String nom = TextField_Search.getText();
            nom = nom.replaceAll("\\s+","").toLowerCase();
        
            for(int i = 0; i < CW.vBateauAmarré.size();i++)
            {
                if(CW.vBateauAmarré.get(i).getEquipage().getAUnEquipage())
                {
                    String capitaine = CW.vBateauAmarré.get(i).getEquipage().getCapitainerie().getNom();
                    capitaine = capitaine.replaceAll("\\s+","").toLowerCase();
                        
                    if(nom.equals(capitaine))
                    {
                        isFind = true;
                        vMarin.add(CW.vBateauAmarré.get(i).getEquipage().getCapitainerie());
                    }

                    if(CW.vBateauAmarré.get(i).getEquipage().getSecond() != null)
                    {
                        String second = CW.vBateauAmarré.get(i).getEquipage().getSecond().getNom();
                        second = second.replaceAll("\\s+","").toLowerCase();
                        
                        if(nom.equals(second))
                        {
                            isFind = true;
                            vMarin.add(CW.vBateauAmarré.get(i).getEquipage().getSecond());
                        }
                    }

                    for(int j = 0; j < CW.vBateauAmarré.get(i).getEquipage().getEquipage().size(); j++)
                    {
                        String passager = CW.vBateauAmarré.get(i).getEquipage().getEquipage().get(j).getNom();
                        passager = passager.replaceAll("\\s+","").toLowerCase();

                        if(nom.equals(passager))
                        {
                            isFind = true;
                            vMarin.add(CW.vBateauAmarré.get(i).getEquipage().getEquipage().get(j));
                        }
                    }
                }
            }

            if(isFind)
            {
                this.dispose();
                InfoMarinWindow imw = new InfoMarinWindow(CW, true, vMarin);
                imw.setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(new JFrame(), "Pas de marin trouvé !", "Erreur", JOptionPane.ERROR_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_Button_SearchActionPerformed

